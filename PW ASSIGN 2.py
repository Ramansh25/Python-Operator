# Perform various assignment operations on a variable:
x = 10

x += 5  # x = x + 5
print("After += 5:", x)  

x -= 3  # x = x - 3
print("After -= 3:", x) 

x *= 2  # x = x * 2
print("After *= 2:", x)  

x /= 4  # x = x / 4
print("After /= 4:", x)  

x %= 5  # x = x % 5
print("After %= 5:", x)  

x **= 3  # x = x ** 3
print("After **= 3:", x)  

x //= 1.5  # x = x // 1.5
print("After //= 1.5:", x)  


